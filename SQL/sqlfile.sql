 -- NATURAL JOINS:
 -- IT HAS 2 BEHAVIOURS
 -- 1. IF THERE IS A CONNECTION BETWEEEN TWO TAABLES THEN IT WILL ACT LIKE INNER JOIN
 -- 2. IF THERE IS NO CONNECTION BETWEEN TWO TABLES THEN IT WILL ACT AS CROSS JOIN/CARTESIAN JOIN 
 -- SYNTAX: ANSi-> SELECT COLNAME/EXP FROM T1 NATURAL JOIN T2.
 
 
