-- CO-RELATED SUBQUERY: A QUERY WRITTEN INSIDE ANOTHER QUERY SUCH THAT OUTER QUERY AND THE INNER QUERY ARE DEPENDENT ON EACH OTHER,
--  THIS IS KNOW AS CO-RELATED SUBQUERY.
-- OUTER QUERY AND INNER QUEERY ARE INTERDEPENDENT TO EACH OTHER
-- POINTS:
-- OUTER QURIES EXECUTES FIRST, BUT PARTIALLY'
-- THE PARTIALLY EXECUTES OUTPUT IS GIVEN AS INPUT TO THE INNER QUERY
-- THE INNER QUERY EXECUTES COMPLETELY AND GENERATE THE OUTPUT
-- THE OUTPUT OF THE INNNER QUERY IS FED AS IN INPUT TO THE OUTER QUERY AND OUTER QUERY PRODUCES RESULT
-- THEREFORE, WE CAN STATE THAT THE OUTER QUERY AND INNER QUERY BOTH ARE INTERDEPENT.
