-- WAQTD DETAILS OF EMPLOYEE AND THEIR MANAGERS NAME IF EMPLOYEE ANUUAL SALARY IS GREATER THAN THEIR MANAGER ANNUAL SALARY
 -- IF EMPLOYEE ARE HIRED AFTER THE EMPLOYEE WHO IS WORKING IN LOCATION OF NEW WORK AND MANAGER ARE HAVING CHARCTER 'A' IN THEIR NAME.
 SELECT E1.EMPNO AS EMP_ID,
       E1.ENAME AS EMPLOYEE_NAME,
       E1.SAL AS EMP_SAL,
       E2.ENAME AS MANAGER_NAME,
       E2.SAL AS MANAGER_SAL
FROM EMP E1
JOIN EMP E2
  ON E1.MGR = E2.EMPNO
WHERE (E1.SAL*12) > (E2.SAL*12)
  AND E1.HIREDATE > ALL (
        SELECT HIREDATE
        FROM EMP
        WHERE DEPTNO IN (
            SELECT DEPTNO
            FROM DEPT
            WHERE LOC = 'NEW YORK'
        )
      )
  AND E2.ENAME LIKE '%A%';
  
  
  
  
